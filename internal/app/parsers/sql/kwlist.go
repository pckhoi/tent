type keywordCategory int

const (
	unreservedKeyword keywordCategory = iota
	colNameKeyword
	typeFuncNameKeyword
	reservedKeyword
)

type sqlKeyword struct {
	name     str
	value    int
	category keywordCategory
}

func scanKeywordLookup(name string) *sqlKeyword {
	name = strings.ToLower(name)
	ind := sort.Search(len(keywords), func(i int) bool {
		return keywords[i].name == name
	})
	if ind < len(keywords) {
		return &keywords[ind]
	}
	return nil
}

